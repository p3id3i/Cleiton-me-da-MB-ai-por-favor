public class metodoExercicios {
    public static double adicao (double luiz1, double luis2){
    return luiz1 + luis2;
    }
       public static double subtracao (double luiz1, double luis2){
    return luiz1 - luis2;
    }
       public static double multiplicacao (double luiz1, double luis2){
    return luiz1 * luis2;
}
  
       public static double divisao (double luiz1, double luis2){
    return luiz1 / luis2;
       }
    }